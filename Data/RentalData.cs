    public static int GetRent(string postcode)
    {
        var district = ExtractDistrict(postcode);
        return Rents.TryGetValue(district, out var rent) ? rent : 1100;
    }

    private static string ExtractDistrict(string postcode)
    {
        var clean = postcode.Trim().ToUpperInvariant().Replace(" ", "");
        // UK postcodes: outward code is everything before the last 3 chars (inward code)
        if (clean.Length > 3)
            return clean[..^3];
        return clean;
    }
